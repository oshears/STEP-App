import Foundation

class Person {
    
    var name:String
    var bio:String
    var major:String
    var image:String
    var role:String
    
    init(name:String, bio:String, major:String, image:String, role:String){
        
        self.name = name
        self.bio = bio
        self.major = major
        self.image = image
        self.role = role
        
    }
    
    
}